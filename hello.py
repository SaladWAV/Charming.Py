#Printing a string of characters.
print("Hello, User.")

#Different ways of printing using a variable.
name = input("Please enter your name: ")
print(f"Hello, {name}")
print("Hello,", name)
print("Hello, " + name)

#How to print quotation marks:
print("Hello, 'David'.")
print('Hello, "David".')
print("Hello, \"David\".")


